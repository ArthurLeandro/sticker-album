/**
 *  Este enumerador é responsável por indicar qual lado o texto devera ser apresentado para o usuário na página
 */
export enum SideOfSticker {
  /**
   * À esquerda da imagem 0
   */
  LEFT = 0,
  /**
   *À direita da imagem 1 
   */
  RIGHT = 1,
  /**
   * Acima da imagem 2 
   */
  ABOVE = 2,
  /**
   * Abaixo da imagem 3
   */
  BELLOW = 3
}
